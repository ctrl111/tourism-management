def replace_chinese_in_sql(file_path):
    """Заменяет китайские тексты на русские в SQL файле"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        original_content = content
        replacements_made = 0
        
        # Замена текстов
        for chinese, russian in SQL_TRANSLATIONS.items():
            if chinese in content:
                # Подсчет замен
                count_before = content.count(chinese)
                # Замена
                content = content.replace(chinese, russian)
                count_after = content.count(russian)
                replacements_made += count_before
        
        # Если были изменения, сохраняем файл
        if content != original_content:
            with open(file_path, 'w', encoding='utf-8') as f:
                f.write(content)
            return True, replacements_made
        
        return False, 0
    
    except Exception as e:
        print(f"Ошибка при обработке файла {file_path}: {e}")
        return False, 0

def process_sql_directory(directory):
    """Обрабатывает все SQL файлы в директории"""
    sql_files = list(Path(directory).rglob('*.sql'))
    total_files = len(sql_files)
    processed_files = 0
    total_replacements = 0
    
    print(f"Найдено {total_files} SQL файлов")
    print("Начинаем обработку...\n")
    
    for sql_file in sql_files:
        modified, replacements = replace_chinese_in_sql(sql_file)
        if modified:
            processed_files += 1
            total_replacements += replacements
            print(f"✓ {sql_file.name} - {replacements} замен")
    
    print(f"\n{'='*60}")
    print(f"Обработка завершена!")
    print(f"Обработано файлов: {processed_files} из {total_files}")
    print(f"Всего замен: {total_replacements}")
    print(f"{'='*60}")

