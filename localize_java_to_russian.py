def replace_chinese_in_file(file_path):
    """Заменяет китайские тексты на русские в файле"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        original_content = content
        replacements_made = 0
        
        # Замена текстов
        for chinese, russian in TRANSLATIONS.items():
            if chinese in content:
                # Замена в строках
                content = content.replace(f'"{chinese}"', f'"{russian}"')
                content = content.replace(f"'{chinese}'", f"'{russian}'")
                replacements_made += content.count(russian) - original_content.count(russian)
        
        # Если были изменения, сохраняем файл
        if content != original_content:
            with open(file_path, 'w', encoding='utf-8') as f:
                f.write(content)
            return True, replacements_made
        
        return False, 0
    
    except Exception as e:
        print(f"Ошибка при обработке файла {file_path}: {e}")
        return False, 0

def process_directory(directory):
    """Обрабатывает все Java файлы в директории"""
    java_files = list(Path(directory).rglob('*.java'))
    total_files = len(java_files)
    processed_files = 0
    total_replacements = 0
    
    print(f"Найдено {total_files} Java файлов")
    print("Начинаем обработку...\n")
    
    for java_file in java_files:
        modified, replacements = replace_chinese_in_file(java_file)
        if modified:
            processed_files += 1
            total_replacements += replacements
            print(f"✓ {java_file.relative_to(directory)} - {replacements} замен")
    
    print(f"\n{'='*60}")
    print(f"Обработка завершена!")
    print(f"Обработано файлов: {processed_files} из {total_files}")
    print(f"Всего замен: {total_replacements}")
    print(f"{'='*60}")

if __name__ == "__main__":
    # Путь к директории с Java файлами
    java_src_dir = "src/main/java"
    
    if os.path.exists(java_src_dir):
        process_directory(java_src_dir)
