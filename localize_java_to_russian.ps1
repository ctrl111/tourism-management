$javaDir = "src\main\java"

if (-not (Test-Path $javaDir)) {
    Write-Host "Директория $javaDir не найдена!" -ForegroundColor Red
    exit 1
}

$javaFiles = Get-ChildItem -Path $javaDir -Filter "*.java" -Recurse
$totalFiles = $javaFiles.Count
$processedFiles = 0
$totalReplacements = 0

Write-Host "Найдено $totalFiles Java файлов" -ForegroundColor Green
Write-Host "Начинаем обработку...`n" -ForegroundColor Yellow

foreach ($file in $javaFiles) {
    $content = Get-Content -Path $file.FullName -Raw -Encoding UTF8
    $originalContent = $content
    $fileReplacements = 0
    
    foreach ($key in $translations.Keys) {
        $value = $translations[$key]
        
        # Замена в двойных кавычках
        $pattern1 = [regex]::Escape("""$key""")
        $replacement1 = """$value"""
        if ($content -match $pattern1) {
            $content = $content -replace $pattern1, $replacement1
            $fileReplacements++
        }
        
        # Замена в одинарных кавычках
        $pattern2 = [regex]::Escape("'$key'")
        $replacement2 = "'$value'"
        if ($content -match $pattern2) {
            $content = $content -replace $pattern2, $replacement2
            $fileReplacements++
        }
    }
    
    if ($content -ne $originalContent) {
        Set-Content -Path $file.FullName -Value $content -Encoding UTF8 -NoNewline
        $processedFiles++
        $totalReplacements += $fileReplacements
        $relativePath = $file.FullName.Replace((Get-Location).Path + "\", "")
        Write-Host "✓ $relativePath - $fileReplacements замен" -ForegroundColor Green
    }
}

Write-Host "`n========================================" -ForegroundColor Cyan
